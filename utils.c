#include "pipex.h"

void	free_paths(char **paths)
{
	int	i;

	i = 0;
	if (paths)
	{
		while (paths[i])
		{
			free(paths[i]);
			i++;
		}
		free(paths);
	}
}

int	is_path_valid(char *path)
{
	return (access(path, X_OK) == 0);
}
